package snake.model.grid

class GridSizeTests extends munit.FunSuite {

  test("GridSize should be able to calculate a power of 2 size") {

    val gridSize: GridSize = GridSize(
      columns = 32,
      rows = 20,
      gridSquareSize = 16
    )

    assertEquals(gridSize.asPowerOf2.value, 32)

  }

}
